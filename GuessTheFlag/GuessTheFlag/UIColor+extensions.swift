import SwiftUI

extension UIColor {
    @objc class var primaryGray: UIColor {
        return UIColor(red: 127/255, green: 127/255, blue: 127/255, alpha: 1)
    }
    @objc class var primaryBlack: UIColor {
        return UIColor(red: 0/255, green: 0/255, blue: 0/255, alpha: 1)
    }
}
